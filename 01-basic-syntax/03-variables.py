# A variable is being used to store
# the results of a calculation or represent a value

# Define a variable for saying hello
greet = "Hello World!"

# then print the variable that we defined
print(greet)

# Also, a variable can store an integer, a float, etc.
# Let's define a variable to store the wallet balance
money = 20

# A cup of Coca-Cola's price
colaPrice = 3.5

# We have to know that a variable's value can be changed
# If we bought a cup of Coca-Cola, then we have...
print("the remaining money is: ", money - colaPrice)  # 16.5

