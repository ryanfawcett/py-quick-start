# The data types in Python
# We can get the data type for literals or variables using the function of type()
greet_type = type("Hello World!")

# 'str' means string
print(greet_type)  # <class 'str'>

money = 20
print(type(money))  # <class 'int'>

colaPrice = 3.5
print(type(colaPrice))  # <class 'float'>

# Note: we have to know that a variable doesn't have a data type
# But the data it stores does.
